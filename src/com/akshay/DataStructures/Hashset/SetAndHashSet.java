package com.akshay.DataStructures.Hashset;

import java.util.HashSet;
import java.util.Set;

public class SetAndHashSet {
    public static void main(String[] args) {
        Set<String> names = new HashSet<>();

        names.add("akshay");
        names.add("pratik");
        names.add("ved");
        names.add("aditi");
        names.add("anjali");

        System.out.println(names);
    }
}
